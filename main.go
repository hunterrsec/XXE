package main

import (
	"fmt"
	"os"

	"github.com/xxeshot/xxeshot/internal/cli"
	"github.com/xxeshot/xxeshot/internal/runner"
)

var version = "1.0.0"

func main() {
	banner()

	opts, err := cli.ParseFlags()
	if err != nil {
		fmt.Fprintf(os.Stderr, "[ERR] %v\n", err)
		os.Exit(1)
	}

	if opts.Version {
		fmt.Printf("xxeshot v%s\n", version)
		os.Exit(0)
	}

	r, err := runner.New(opts)
	if err != nil {
		fmt.Fprintf(os.Stderr, "[ERR] %v\n", err)
		os.Exit(1)
	}

	r.Run()
}

